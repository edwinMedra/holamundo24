            <h5 class="card-title text-center aling-items-center mt-4 mb-5 ps-5 pe-5">¿Cuál es la función de un asistente para personas discapacitadas?</h5>
            <p class="card-text pe-5 ps-5">
              Un asistente personal en una aerolínea puede proporcionar una variedad de servicios para atender a personas discapacitadas y garantizar que tengan una experiencia de viaje cómoda y segura. Aquí hay algunas formas en las que un asistente personal podría ayudar:
            </p>
            <ul class="list-card pe-5 ps-5">
              <li class=" mt-4"><strong>Asistencia en el aeropuerto:</strong> El asistente puede ayudar a la persona discapacitada desde el momento en que llega al aeropuerto hasta que aborda el avión. Esto puede incluir ayudar con el check-in, la obtención de pases de embarque, el paso por el control de seguridad y la navegación por el aeropuerto.</li>
              <li class=" mt-4"><strong>Acompañamiento y orientación:</strong> Durante el tiempo en el aeropuerto, el asistente puede acompañar a la persona discapacitada a lo largo de todo el proceso y proporcionar orientación sobre las ubicaciones de las puertas de embarque, los servicios disponibles y otras áreas relevantes del aeropuerto.</li>
              <li class=" mt-4"><strong>Asistencia en el embarque:</strong> El asistente puede ayudar a la persona discapacitada a abordar el avión de manera segura y cómoda. Esto puede incluir el uso de equipos de asistencia, como sillas de ruedas o carritos de transporte, para garantizar un acceso fácil a la cabina.</li>
              <li class=" mt-4"><strong>Asistencia durante el vuelo:</strong> Durante el vuelo, el asistente puede proporcionar ayuda adicional según sea necesario. Esto puede incluir ayudar a la persona discapacitada a encontrar su asiento, obtener y guardar artículos personales, y proporcionar asistencia en comunicación con la tripulación de cabina.</li>
              <li class=" mt-4"><strong>Asistencia en tierra:</strong> Al llegar a su destino, el asistente puede ayudar a la persona discapacitada a desembarcar del avión y navegar por el aeropuerto para llegar a su destino final. Esto puede incluir el uso de equipos de asistencia y la coordinación con servicios terrestres, como transporte desde el aeropuerto.</li>
            </ul>
